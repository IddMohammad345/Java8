package Java8_PracticeFrom_drive;

public class SwapTwoNumbersWithoutUsingThirdVariable {
    public static void main(String[] args) {
        int a =7;
        int b=8;

        a=a+b;//a=15
       b= a-b;//b=7
       a=a-b;//a=8

        System.out.println(" a = "+a+
                           "\n b = "+b);

    }
}
